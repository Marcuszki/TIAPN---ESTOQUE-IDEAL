
function carregarDados(id, nome, descricao, quantidade, custo, venda) {
    document.getElementById('edit-id').value = id;
    document.getElementById('edit-nome').value = nome;
    document.getElementById('edit-descricao').value = descricao;
    document.getElementById('edit-quantidade').value = quantidade;
    document.getElementById('edit-custo').value = custo;
    document.getElementById('edit-venda').value = venda;
}

